package Bean;

public class Tongji {
	private String weifahuo;
	private String yiwancheng;
	private String yifahuo;
	public String getWeifahuo() {
		return weifahuo;
	}
	public void setWeifahuo(String weifahuo) {
		this.weifahuo = weifahuo;
	}
	public String getYiwancheng() {
		return yiwancheng;
	}
	public void setYiwancheng(String yiwancheng) {
		this.yiwancheng = yiwancheng;
	}
	public String getYifahuo() {
		return yifahuo;
	}
	public void setYifahuo(String yifahuo) {
		this.yifahuo = yifahuo;
	}
	@Override
	public String toString() {
		return "Tongji [weifahuo=" + weifahuo + ", yiwancheng=" + yiwancheng + ", yifahuo=" + yifahuo + "]";
	}
	
	
	
}
